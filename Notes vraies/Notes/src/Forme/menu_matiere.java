     public final void liste_table_matiere(){
        String[][] tableau_matiere;
        String[] table_titre={"Code Matiere", "Libelle", "Coefficient"};
        int nbLigne,nbColonne;
        nbLigne=nbColonne=0;
        
         try{
           Class.forName("com.mysql.jdbc.Driver");
           Connection connexion=DriverManager.getConnection("jdbc:mysql://localhost/etablissement","root","");
           Statement instruction=connexion.createStatement();
	   ResultSet resultat=instruction.executeQuery("SELECT * FROM matiere");
           
           while(resultat.next())
               nbLigne++;
               nbColonne=3;
           while(resultat.previous());
           
           tableau_matiere=new String[nbLigne][nbColonne];
           int i,j;
           i=0;
           while(resultat.next()){
                    j=0;
                    while(j<nbColonne){
                    tableau_matiere[i][j]=resultat.getString(j+1);
                    j++;}
                    i++;
           }
          table_matiere.setModel(new javax.swing.table.DefaultTableModel(tableau_matiere,table_titre));
          jScrollPane1.setViewportView(table_matiere);
        }catch(Exception ex){}
     }
    
